char	*ft_strlowcase(char *str);

char	*ft_strlowcase(char *str)
{
	char	*copy_prt_str;

	copy_prt_str = str;
	while (*str != '\0')
	{
		if (*str >= 'A' && *str <= 'Z')
		{
			*str = *str + 32;
		}		
		str++;
	}
	return (copy_prt_str);
}
