char	*ft_strupcase(char *str);

char	*ft_strupcase(char *str)
{
	char	*copy_prt_str;

	copy_prt_str = str;
	while (*str != '\0')
	{
		if (*str >= 'a' && *str <= 'z')
		{
			*str = *str - 32;
		}		
		str++;
	}
	return (copy_prt_str);
}
